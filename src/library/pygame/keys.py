from enum import Enum
from pygame.locals import (
    K_UP,
    K_DOWN,
    K_LEFT,
    K_RIGHT,
    QUIT,
    K_ESCAPE,
    KEYDOWN,
    K_SPACE
)

class Keys(Enum):
    K_UP = K_UP
    K_DOWN = K_DOWN
    K_LEFT = K_LEFT
    K_RIGHT = K_RIGHT
    QUIT = QUIT
    K_ESCAPE = K_ESCAPE
    KEYDOWN = KEYDOWN
    K_SPACE = K_SPACE
